// C ABI bridge between the iOS Swift shell and Anki's Rust core (rslib).
//
// Design mirrors pylib/rsbridge/lib.rs but uses a plain C ABI instead of PyO3,
// because the Swift side communicates via raw protobuf bytes (no typed bindings
// needed — swift-protobuf handles encoding/decoding on the Swift side).
//
// Error convention for anki_run_method:
//   return 0  → success; *out / *out_len contain the response proto bytes.
//   return -1 → error;   *out / *out_len contain a BackendError proto bytes.
// In both cases the caller must pass the buffer back to anki_free_bytes when
// done.

use std::ffi::c_char;

use anki::backend::init_backend;
use anki::backend::Backend;

// ---------------------------------------------------------------------------
// Public C ABI
// ---------------------------------------------------------------------------

/// Open a backend from a serialised `anki_proto::backend::BackendInit` message.
/// Returns a heap-allocated Backend on success, or null on failure.
/// The caller owns the returned pointer and must eventually pass it to
/// `anki_close_backend`.
#[no_mangle]
pub extern "C" fn anki_open_backend(init_msg: *const u8, len: usize) -> *mut Backend {
    if init_msg.is_null() {
        return std::ptr::null_mut();
    }
    let bytes = unsafe { std::slice::from_raw_parts(init_msg, len) };
    match init_backend(bytes) {
        Ok(backend) => Box::into_raw(Box::new(backend)),
        Err(_) => std::ptr::null_mut(),
    }
}

/// Dispatch a protobuf RPC call to the backend.
///
/// `service` and `method` are the integer indices generated by the rslib build
/// (see OUT_DIR/backend.rs).  The request is `input[..len]`.  On return,
/// `*out` points to a freshly heap-allocated buffer of `*out_len` bytes
/// (either the response proto or a BackendError proto).
///
/// Returns 0 on success, -1 on error (BackendError in *out/*out_len).
/// The caller must pass *out to `anki_free_bytes(*out, *out_len)` when done.
#[no_mangle]
pub extern "C" fn anki_run_method(
    backend: *mut Backend,
    service: u32,
    method: u32,
    input: *const u8,
    len: usize,
    out: *mut *mut u8,
    out_len: *mut usize,
) -> i32 {
    if backend.is_null() || out.is_null() || out_len.is_null() {
        return -1;
    }
    let backend = unsafe { &*backend };
    let in_bytes = unsafe { std::slice::from_raw_parts(input, len) };

    let (bytes, rc) = match backend.run_service_method(service, method, in_bytes) {
        Ok(v) => (v, 0),
        Err(v) => (v, -1),
    };
    write_output(bytes, out, out_len);
    rc
}

/// Free a buffer previously returned via `anki_run_method`.
#[no_mangle]
pub extern "C" fn anki_free_bytes(ptr: *mut u8, len: usize) {
    if ptr.is_null() {
        return;
    }
    unsafe {
        drop(Vec::from_raw_parts(ptr, len, len));
    }
}

/// Close the backend and free all associated resources.
#[no_mangle]
pub extern "C" fn anki_close_backend(backend: *mut Backend) {
    if !backend.is_null() {
        unsafe { drop(Box::from_raw(backend)) };
    }
}

