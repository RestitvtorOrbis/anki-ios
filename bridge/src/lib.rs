// ---------------------------------------------------------------------------
// Internal helpers
// ---------------------------------------------------------------------------

fn write_output(bytes: Vec<u8>, out: *mut *mut u8, out_len: *mut usize) {
    let len = bytes.len();
    let mut bytes = bytes.into_boxed_slice();
    let ptr = bytes.as_mut_ptr();
    std::mem::forget(bytes);
    unsafe {
        *out = ptr;
        *out_len = len;
    }
}

// ---------------------------------------------------------------------------
// Tests — host-side (Linux/macOS), no iOS target required
// ---------------------------------------------------------------------------

#[cfg(test)]
mod tests {
    use super::*;
    use prost::Message;

    fn make_init_bytes() -> Vec<u8> {
        anki_proto::backend::BackendInit {
            preferred_langs: vec!["en".to_string()],
            ..Default::default()
        }
        .encode_to_vec()
    }

    #[test]
    fn open_and_close_backend() {
        let init = make_init_bytes();
        let backend = anki_open_backend(init.as_ptr(), init.len());
        assert!(!backend.is_null(), "init_backend returned null");
        anki_close_backend(backend);
    }

    // Service/method indices below were read from the generated dispatch code
    // (OUT_DIR/backend.rs, produced by rslib's build script from the proto
    // descriptor pool).  Backend services have odd indices; collection-level
    // services the even ones.  These must stay in sync with the Swift enum
    // generated in Phase 2 (scripts/gen-swift-proto.sh derives both from the
    // same source).
    const SERVICE_BACKEND_COLLECTION: u32 = 3;
    const METHOD_OPEN_COLLECTION: u32 = 0;
    const SERVICE_BACKEND_DECKS: u32 = 7;
    const METHOD_DECK_TREE: u32 = 4;

    fn run_ffi(
        backend: *mut Backend,
        service: u32,
        method: u32,
        input: &[u8],
    ) -> (i32, Vec<u8>) {
        let mut out: *mut u8 = std::ptr::null_mut();
        let mut out_len: usize = 0;
        let rc = anki_run_method(
            backend,
            service,
            method,
            input.as_ptr(),
            input.len(),
            &mut out,
            &mut out_len,
        );
        let bytes = unsafe { std::slice::from_raw_parts(out, out_len) }.to_vec();
        anki_free_bytes(out, out_len);
        (rc, bytes)
    }

    /// Phase 1 success criterion: open a backend, create a collection in a
    /// temp dir, and run a deck_tree RPC — all through the C ABI.
    #[test]
    fn open_collection_and_deck_tree() {
        let dir = tempfile::tempdir().unwrap();
        let media_dir = dir.path().join("collection.media");
        std::fs::create_dir_all(&media_dir).unwrap();

        let init_bytes = make_init_bytes();
        let backend = anki_open_backend(init_bytes.as_ptr(), init_bytes.len());
        assert!(!backend.is_null());

        let open_req = anki_proto::collection::OpenCollectionRequest {
            collection_path: dir
                .path()
                .join("collection.anki2")
                .to_str()
                .unwrap()
                .to_string(),
            media_folder_path: media_dir.to_str().unwrap().to_string(),
            media_db_path: dir
                .path()
                .join("collection.media.db")
                .to_str()
                .unwrap()
                .to_string(),
        };
        let (rc, err) = run_ffi(
            backend,
            SERVICE_BACKEND_COLLECTION,
            METHOD_OPEN_COLLECTION,
            &open_req.encode_to_vec(),
        );
        assert_eq!(
            rc,
            0,
            "OpenCollection failed: {:?}",
            anki_proto::backend::BackendError::decode(err.as_slice())
        );

        let deck_tree_req = anki_proto::decks::DeckTreeRequest { now: 1 };
        let (rc, out) = run_ffi(
            backend,
            SERVICE_BACKEND_DECKS,
            METHOD_DECK_TREE,
            &deck_tree_req.encode_to_vec(),
        );
        assert_eq!(
            rc,
            0,
            "DeckTree failed: {:?}",
            anki_proto::backend::BackendError::decode(out.as_slice())
        );
        let tree = anki_proto::decks::DeckTreeNode::decode(out.as_slice()).unwrap();
        // A fresh collection contains the single "Default" deck under the root.
        assert_eq!(tree.children.len(), 1);

        anki_close_backend(backend);
    }
}
